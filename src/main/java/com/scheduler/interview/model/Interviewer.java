package com.scheduler.interview.model;

import jakarta.persistence.*;
import java.time.LocalDateTime;

@Entity
@Table(name = "interviewers")
public class Interviewer {
    
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    
    @Column(nullable = false)
    private String name;
    
    @Column(nullable = false, unique = true)
    private String email;
    
    @Column(name = "max_weekly_interviews")
    private Integer maxWeeklyInterviews = 10;
    
    @Column(name = "created_at")
    private LocalDateTime createdAt;
    
    @PrePersist
    protected void onCreate() { createdAt = LocalDateTime.now(); }
    
    // Constructors
    public Interviewer() {}
    public Interviewer(String name, String email, Integer max) {
        this.name = name;
        this.email = email;
        this.maxWeeklyInterviews = max;
    }
    
    // Getters & Setters
    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }
    public Integer getMaxWeeklyInterviews() { return maxWeeklyInterviews; }
    public void setMaxWeeklyInterviews(Integer max) { this.maxWeeklyInterviews = max; }
    public LocalDateTime getCreatedAt() { return createdAt; }
}
